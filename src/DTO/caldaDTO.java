package DTO;
public class caldaDTO {
    private int id_calda;
    private String descricao_calda, adicional_calda;

    /**
     * @return the id_calda
     */
    public int getId_calda() {
        return id_calda;
    }

    /**
     * @param id_calda the id_calda to set
     */
    public void setId_calda(int id_calda) {
        this.id_calda = id_calda;
    }

    /**
     * @return the descricao_calda
     */
    public String getDescricao_calda() {
        return descricao_calda;
    }

    /**
     * @param descricao_calda the descricao_calda to set
     */
    public void setDescricao_calda(String descricao_calda) {
        this.descricao_calda = descricao_calda;
    }

    /**
     * @return the adicional_calda
     */
    public String getAdicional_calda() {
        return adicional_calda;
    }

    /**
     * @param adicional_calda the adicional_calda to set
     */
    public void setAdicional_calda(String adicional_calda) {
        this.adicional_calda = adicional_calda;
    }
}
