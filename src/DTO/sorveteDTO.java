package DTO;
public class sorveteDTO {
    private int id_sorvete, cod_calda;
    private String tipo_sorvete, sabor_sorvete;
    float preco_sorvete;

    /**
     * @return the id_sorvete
     */
    public int getId_sorvete() {
        return id_sorvete;
    }

    /**
     * @param id_sorvete the id_sorvete to set
     */
    public void setId_sorvete(int id_sorvete) {
        this.id_sorvete = id_sorvete;
    }

    /**
     * @return the tipo_sorvete
     */
    public String getTipo_sorvete() {
        return tipo_sorvete;
    }

    /**
     * @param tipo_sorvete the tipo_sorvete to set
     */
    public void setTipo_sorvete(String tipo_sorvete) {
        this.tipo_sorvete = tipo_sorvete;
    }

    /**
     * @return the preco_sorvete
     */
    public float getPreco_sorvete() {
        return preco_sorvete;
    }

    /**
     * @param preco_sorvete the preco_sorvete to set
     */
    public void setPreco_sorvete(float preco_sorvete) {
        this.preco_sorvete = preco_sorvete;
    }

    /**
     * @return the cod_calda
     */
    public int getCod_calda() {
        return cod_calda;
    }

    /**
     * @param cod_calda the cod_calda to set
     */
    public void setCod_calda(int cod_calda) {
        this.cod_calda = cod_calda;
    }

    /**
     * @return the sabor_sorvete
     */
    public String getSabor_sorvete() {
        return sabor_sorvete;
    }

    /**
     * @param sabor_sorvete the sabor_sorvete to set
     */
    public void setSabor_sorvete(String sabor_sorvete) {
        this.sabor_sorvete = sabor_sorvete;
    }
}



