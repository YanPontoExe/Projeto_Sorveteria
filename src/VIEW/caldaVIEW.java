    private void cadastrarCalda(){
        caldaDTO objcaldaDTO = new caldaDTO();
        
        String calda, preco;
        
        calda=txtCalda.getText();
        preco=txtPreco.getText();
        
        
        objcaldaDTO.setDescricao_calda(calda);
        objcaldaDTO.setAdicional_calda(preco);
        
        
        caldaDAO objcaldaDAO = new caldaDAO(); 
        objcaldaDAO.cadastrarCalda(objcaldaDTO); 
    }
    
    private void Limpar(){
        txtCalda.setText("");
        txtPreco.setText("");
        txtId.setText("");
        txtCalda.requestFocus();
    }
    
    private void listarValores(){
        try {
            caldaDAO objcaldaDAO = new caldaDAO();
            
            DefaultTableModel model = (DefaultTableModel) tabelaCalda.getModel();
            model.setNumRows(0);
            
            ArrayList<caldaDTO> lista = objcaldaDAO.pesquisarCalda();
            
            for(int num=0; num<lista.size(); num++){
                model.addRow(new Object[]{
                        lista.get(num).getId_calda(),
                        lista.get(num).getDescricao_calda(),
                        lista.get(num).getAdicional_calda()
                    });
                        
            }
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "frmCaldaView Listar valores: "+erro);
        }
        
    }
    
    private void carregarCampos(){
        int setar = tabelaCalda.getSelectedRow();
        
        
        
       
        txtId.setText(tabelaCalda.getModel().getValueAt(setar, 0).toString());
        txtCalda.setText(tabelaCalda.getModel().getValueAt(setar, 1).toString());
        txtPreco.setText(tabelaCalda.getModel().getValueAt(setar, 2).toString());
        
    }
    
    private void alterarCalda(){
        int id_calda;
        String descricao_calda, adicional_calda;
        
        id_calda=Integer.parseInt(txtId.getText());
        descricao_calda=txtCalda.getText();
        adicional_calda=txtPreco.getText();
        
        caldaDTO objcaldaDTO = new caldaDTO();
        objcaldaDTO.setId_calda(id_calda);
        objcaldaDTO.setDescricao_calda(descricao_calda);
        objcaldaDTO.setAdicional_calda(adicional_calda);
        
        caldaDAO objcaldaDAO = new caldaDAO();
        objcaldaDAO.alterarCalda(objcaldaDTO);
        
    }
    
    private void excluirCalda(){
        int id_calda=Integer.parseInt(txtId.getText());
        
        caldaDTO objcaldaDTO = new caldaDTO();
        objcaldaDTO.setId_calda(id_calda);
        
        caldaDAO objcaldaDAO = new caldaDAO();
        objcaldaDAO.excluirCalda(objcaldaDTO);
    }
    
