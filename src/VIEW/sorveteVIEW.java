    private void cadastrarSorvete(){
        sorveteDTO objsorveteDTO = new sorveteDTO();
        
        String  sabor;
        float preco;
        
        preco=Float.parseFloat(txtPreco.getText());
        sabor=txtSabor.getText();
        
        
        objsorveteDTO.setPreco_sorvete(preco);
        objsorveteDTO.setSabor_sorvete(sabor);
        
        
        sorveteDAO objsorveteDAO = new sorveteDAO(); 
        objsorveteDAO.cadastrarSorvete(objsorveteDTO);
        
    }
    
    private void Limpar(){
        txtPreco.setText("");
        txtId.setText("");
        txtSabor.setText("");
        txtSabor.requestFocus();
    }
    
    private void listarValores(){
        try {
            sorveteDAO objsorveteDAO = new sorveteDAO();
            
            DefaultTableModel model = (DefaultTableModel) tabelaSorvete.getModel();
            model.setNumRows(0);
            
            ArrayList<sorveteDTO> lista = objsorveteDAO.pesquisarSorvete();
            
            for(int num=0; num<lista.size(); num++){
                model.addRow(new Object[]{
                        lista.get(num).getId_sorvete(),
                        lista.get(num).getPreco_sorvete(),
                        lista.get(num).getSabor_sorvete()
                    });
                        
            }
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "frmSorveteView Listar valores: "+erro);
        }
        
    }
    
    
    
    private void carregarCampos(){
        int setar = tabelaSorvete.getSelectedRow();
        
        
        
       
        txtId.setText(tabelaSorvete.getModel().getValueAt(setar, 0).toString());
        txtPreco.setText(tabelaSorvete.getModel().getValueAt(setar, 1).toString());
        txtSabor.setText(tabelaSorvete.getModel().getValueAt(setar, 2).toString());
        
    }
    
    private void alterarSorvete(){
        int id_sorvete;
        String sabor_sorvete;
        float preco_sorvete;
        
        id_sorvete=Integer.parseInt(txtId.getText());
        preco_sorvete=Float.parseFloat(txtPreco.getText());
        sabor_sorvete=txtSabor.getText();
        
        sorveteDTO objsorveteDTO = new sorveteDTO();
        objsorveteDTO.setId_sorvete(id_sorvete);
        objsorveteDTO.setPreco_sorvete(preco_sorvete);
        objsorveteDTO.setSabor_sorvete(sabor_sorvete);
        
        sorveteDAO objsorveteDAO = new sorveteDAO();
        objsorveteDAO.alterarSorvete(objsorveteDTO);
        
    }
    
    private void excluirSorvete(){
        int id_sorvete=Integer.parseInt(txtId.getText());
        
        sorveteDTO objsorveteDTO = new sorveteDTO();
        objsorveteDTO.setId_sorvete(id_sorvete);
        
        sorveteDAO objsorveteDAO = new sorveteDAO();
        objsorveteDAO.excluirSorvete(objsorveteDTO);
    }
    
   
   
   
    
